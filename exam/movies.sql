create table MOVIE (
ID_MOVIE int CONSTRAINT ID_MOVIE_PK PRIMARY KEY,
MOVIE_NAME NVARCHAR(100),
MOV_YEAR INT,
COUNTRY NVARCHAR(100)
);

create table STAR (
ID_STAR int CONSTRAINT ID_STAR_PK PRIMARY KEY,
STAR_NAME NVARCHAR(100),
BIRTH NVARCHAR(100)
);

create table STAR_MOVIE (
ID_STAR int, CONSTRAINT FK_ID_STAR_SM
                                    FOREIGN KEY (ID_STAR)
                                    REFERENCES STAR(ID_STAR),
ID_MOVIE int, CONSTRAINT FK_ID_MOVIE_SM
                                    FOREIGN KEY (ID_MOVIE)
                                    REFERENCES MOVIE(ID_MOVIE),
STAR_TYPE NVARCHAR(100)
);

